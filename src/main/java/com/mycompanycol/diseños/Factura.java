      public void dtopro()throws SQLException{
            persona = new DtoProducto();
                    persona.id= ID.getText() ;
                    persona.descripcion = vDescripcion.getText();
                    persona.cantidad = Integer.parseInt(vCantidad.getText());
                    persona.precio = Double.parseDouble(vPrecio.getText());
                    persona.costo = Double.parseDouble(vCosto.getText());
                    persona.itbis = Double.parseDouble(vItbis.getText());
            
            Connection dbConnection = null;
        PreparedStatement preparedStatementInsert = null;
       

        String insertTableSQL = "INSERT INTO productos"
                + "(idproducto,descripcion,cantidad,precio,costo,tax) VALUES"
                + "(?,?,?,?,?,?)";

       

        try {
            dbConnection = getDBConnection();

            dbConnection.setAutoCommit(false);

            preparedStatementInsert = dbConnection.prepareStatement(insertTableSQL);
            preparedStatementInsert.setString(1,persona.id );
            preparedStatementInsert.setString(2,persona.descripcion );
            preparedStatementInsert.setInt(3,persona.cantidad);
            preparedStatementInsert.setDouble(4,persona.precio );
            preparedStatementInsert.setDouble(5,persona.costo );
            preparedStatementInsert.setDouble(6,persona.itbis);
            preparedStatementInsert.executeUpdate();

            

            dbConnection.commit();

            System.out.println("Done!");

        } catch (SQLException e) {

            System.out.println(e.getMessage());
            dbConnection.rollback();

        } finally {

            if (preparedStatementInsert != null) {
                preparedStatementInsert.close();
            }

            

            if (dbConnection != null) {
                dbConnection.close();
            }

        }
        
    }
      
      
